function tithiForecastParagraph(tithiNum: number): string {
  if (tithiNum >= 1 && tithiNum <= 30) return TITHI_FORECAST[tithiNum - 1];
  return "Титхи вне диапазона 1–30; ориентируйтесь на общий протокол дня в приложении.";
}


function nakshatraTransitParagraph(transitMoonNak: string): string {
  const note =
    NAK_TRANSIT_NOTE[transitMoonNak] ??
    "Лунный фон дня нейтрален; придерживайтесь протокола и наблюдайте за телом вечером и утром.";
  return `Транзитная Луна в накшатре «${transitMoonNak}». ${note}`;
}

function natalWeaveParagraph(
  natal: NatalProfile,
  snap: Snap,
  natalMoonD1: string,
  natalSunD1: string,
): string {
  const transitMoonNak = nakName(snap.moon);
  const transitSunNak = nakName(snap.sun);
  const iNatM = nakIndex(natal.moon);
  const iTrM = nakIndex(snap.moon);
  const sep = nakSeparationIndex(iNatM, iTrM);

  const parts: string[] = [];

  parts.push(
    `На вашей натальной карте Луна в накшатре «${natalMoonD1}», Солнце — в накшатре «${natalSunD1}». Сегодня транзитное Солнце находится в накшатре «${transitSunNak}».`,
  );

  if (transitMoonNak === natalMoonD1) {
    parts.push(
      "Транзитная Луна совпадает с накшатрой вашей натальной Луны — день личного лунного резонанса: тело острее реагирует на ритм сна, на объём жидкости и на размер обеда.",
    );
  } else if (sep === 13 || sep === 14) {
    parts.push(
      "Транзитная Луна стоит примерно напротив вашей натальной Луны на круге из двадцати семи накшатр — выше чувствительность к полноте тела, ко сну и к соли; обед и вечер держите строже, чем обычно.",
    );
  } else if (sep <= 3) {
    parts.push(
      "Транзитная Луна близка к вашей натальной Луне на круге накшатр — мягкая поддержка привычного тона дня; время приёма пищи не ломайте.",
    );
  } else {
    parts.push(
      `Между натальной и транзитной Луной по кругу накшатр — ${sep} шагов: контраст между вашей постоянной лунной опорой и сегодняшним транзитом даёт колебания аппетита и настроения; опирайтесь на фиксированный протокол, а не на импульс.`,
    );
  }

  if (transitSunNak === natalSunD1) {
    parts.push(
      "Транзитное Солнце в накшатре вашего натального Солнца — день ясного намерения в режиме; проще удержать дисциплину обеда.",
    );
  }

  return parts.join(" ");
}

export interface NatalDayForecast {
  /** Заголовок блока на экране «Сегодня». */
  title: string;
  /** Полное имя титхи (как в UI). */
  tithi_label: string;
  /** Абзацы: титхи → накшатра → связка с наталом. */
  paragraphs: string[];
}

/**
 * Прогноз по натальной карте (профиль NATAL) на календарную дату: титхи + транзит Луны в накшатре + натальные Луна/Солнце.
 * Полностью детерминированный текст, без LLM.
 */
export function buildNatalDayForecast(
  natal: NatalProfile,
  snap: Snap,
  tithiNameRu: string,
): NatalDayForecast {
  const natalMoonD1 = nakshatraD1(natal.moon);
  const natalSunD1 = nakshatraD1(natal.sun);
  const transitMoonNak = nakName(snap.moon);

  const p1 = `Сегодня титхи «${tithiNameRu}» (лунный день ${snap.tithi}). ${tithiForecastParagraph(snap.tithi)}`;

  const p2 = nakshatraTransitParagraph(transitMoonNak);
  const p3 = natalWeaveParagraph(natal, snap, natalMoonD1, natalSunD1);

  return {
    title: "Прогноз по вашей натальной карте",
    tithi_label: tithiNameRu,
    paragraphs: [p1, p2, p3],
  };
}
